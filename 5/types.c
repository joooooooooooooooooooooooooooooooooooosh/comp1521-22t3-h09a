#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>

int main(int argc, char **argv) {
    long l;
    printf("%lu\n", sizeof(l));

    // 4 * 10 + 2 * 1  = 42
    // 5 * 8 + 2 * 1 = 42
    // 2 * 16 + 10 * 1 = 42
    printf("%d %d %d %d\n", 42, 052, 0x2a, 0b101010);

    uint16_t a = 0x5432, b = 0xABDE, c = 0x0001;

    // a     = 0b0101 0100 0011 0010
    // b     = 0b1010 1011 1101 1110
    // a | b = 0b1111 1111 1111 1110
    printf("0x%04x\n", a | b);

    // a     = 0b0101 0100 0011 0010
    // b     = 0b1010 1011 1101 1110
    // a & b = 0b0000 0000 0001 0010
    printf("0x%04x\n", a & b);

    // a     = 0b0101 0100 0011 0010
    // b     = 0b1010 1011 1101 1110
    // a ^ b = 0b1111 1111 1110 1100
    printf("0x%04x\n", a ^ b);

    // a     = 0b0101 0100 0011 0010
    // ~a    = 0b1010 1011 1100 1101
    printf("0x%04x\n", (uint16_t)~a);

    // b     = 0b1010 1011 1101 1110
    // ~b    = 0b0101 0100 0010 0001
    // a     = 0b0101 0100 0011 0010
    // a ^ ~b= 0b0101 0100 0010 0000
    printf("0x%04x\n", a & ~b);

    // c     = 0b0000 0000 0000 0001
    // c << 6= 0b0000 0000 0100 0000
    printf("0x%04x\n", c << 6);

    // a     = 0b0101 0100 0011 0010
    // a >> 4= 0b0000 0101 0100 0011
    printf("0x%04x\n", a >> 4);

    return EXIT_SUCCESS;
}
